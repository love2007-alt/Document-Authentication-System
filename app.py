@app.route('/verify-qr', methods=['GET', 'POST'])

def verify_qr():

    result = None

    if request.method == 'POST':

        qr_file = request.files['qr_image']

        qr_path = os.path.join(
            'uploads',
            qr_file.filename
        )

        qr_file.save(qr_path)

        image = Image.open(qr_path)

        decoded_objects = decode(image)

        if decoded_objects:

            qr_data = decoded_objects[0].data.decode(
                'utf-8'
            )

            blockchain = load_blockchain()

            for block in blockchain:

                if block['current_hash'] in qr_data:

                    result = f"""
                    AUTHENTIC DOCUMENT

                    File:
                    {block['filename']}

                    Hash:
                    {block['current_hash']}
                    """

                    break

            if not result:

                result = """
                INVALID QR CODE
                """

        else:

            result = """
            QR CODE NOT DETECTED
            """

    return render_template(
        'verify_qr.html',
        result=result
    )

@app.route('/verify-hash', methods=['GET', 'POST'])

def verify_hash():

    result = None

    if request.method == 'POST':

        entered_hash = request.form['hash'].strip()

        blockchain = load_blockchain()

        found = False

        for block in blockchain:

            if block['current_hash'] == entered_hash:

                found = True

                result = f"""
                AUTHENTIC HASH VERIFIED

                File:
                {block['filename']}

                Timestamp:
                {block['timestamp']}

                Block Number:
                {block['index']}
                """

                break

        if not found:

            result = """
            INVALID OR UNKNOWN HASH
            """

    return render_template(
        'verify_hash.html',
        result=result
    )

