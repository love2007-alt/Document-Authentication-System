@app.route('/upload', methods=['POST'])

def upload_file():

    file = request.files['document']

    if file:

        filepath = os.path.join(
            app.config['UPLOAD_FOLDER'],
            file.filename
        )

        file.save(filepath)

        file_hash = generate_hash(filepath)

        risk_level = analyze_document(file)

        blockchain = load_blockchain()

        previous_hash = "0000"

        if len(blockchain) > 0:

            previous_hash = blockchain[-1]['current_hash']

        block = {

            'index': len(blockchain) + 1,

            'filename': file.filename,

            'timestamp': datetime.now().strftime(
                "%d-%m-%Y %I:%M:%S %p"
            ),

            'previous_hash': previous_hash,

            'current_hash': file_hash
        }

        blockchain.append(block)

        save_blockchain(blockchain)

        sync_nodes(blockchain)

        account = web3.eth.accounts[0]

        tx_hash = contract.functions.uploadDocument(

            file.filename,
            file_hash

        ).transact({

            'from': account

        })

        web3.eth.wait_for_transaction_receipt(
            tx_hash
        )

        qr_data = f"""
        Document Name: {file.filename}

        SHA-256 Hash:
        {file_hash}
        """

        qr = qrcode.make(qr_data)

        qr_filename = f"{file.filename}.png"

        qr_path = os.path.join(
            'static/qrcodes',
            qr_filename
        )

        qr.save(qr_path)

        report_file = create_report(
            file.filename,
            file_hash
        )

        return render_template(

            'result.html',

            file_hash=file_hash,

            qr_file=qr_filename,

            report_file=report_file,

            risk_level=risk_level

        )

    return "File Upload Failed"

@app.route('/verify', methods=['POST'])

def verify_file():

    file = request.files['document']

    if file:

        filepath = os.path.join(
            app.config['UPLOAD_FOLDER'],
            file.filename
        )

        file.save(filepath)

        new_hash = generate_hash(filepath)

        blockchain = load_blockchain()

        original_hash = None

        for block in blockchain:

            if block['filename'] == file.filename:

                original_hash = block['current_hash']

        if original_hash == new_hash:

            return render_template(
                'verify_result.html',
                status="authentic"
            )

        else:

            return render_template(
                'verify_result.html',
                status="tampered"
            )

    return "Verification Failed"

