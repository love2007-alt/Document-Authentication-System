app.config['UPLOAD_FOLDER'] = UPLOAD_FOLDER

def load_blockchain():

    try:

        with open('blockchain.json', 'r') as file:

            return json.load(file)

    except:

        return []

def save_blockchain(blockchain_data):

    with open('blockchain.json', 'w') as file:

        json.dump(
            blockchain_data,
            file,
            indent=4
        )

def create_report(filename, file_hash):

    blockchain = load_blockchain()

    timestamp = "Unknown"

    for block in blockchain:

        if block["filename"] == filename:

            timestamp = block["timestamp"]

            break

    report_filename = f"{filename}_report.pdf"

    report_path = os.path.join(
        'reports',
        report_filename
    )

    pdf = SimpleDocTemplate(report_path)

    styles = getSampleStyleSheet()

    content = []

    title = Paragraph(
        "<b>DOCUMENT AUTHENTICATION REPORT</b>",
        styles['Title']
    )

    content.append(title)

    content.append(Spacer(1, 20))

    lines = [

        f"<b>Generated By:</b> Shield Squad",

        f"<b>File Name:</b> {filename}",

        f"<b>Timestamp:</b> {timestamp}",

        f"<b>SHA-256 Hash:</b> {file_hash}",

        f"<b>Verification Status:</b> AUTHENTIC",

        f"<b>Blockchain Security:</b> ACTIVE",

        f"<b>Web3 Verification:</b> ENABLED"
    ]

    for line in lines:

        paragraph = Paragraph(
            line,
            styles['BodyText']
        )

        content.append(paragraph)

        content.append(Spacer(1, 12))

    pdf.build(content)

    return report_filename

def analyze_document(file):

    risky_extensions = [
        '.exe',
        '.bat',
        '.js',
        '.apk'
    ]

    filename = file.filename.lower()

    risk_level = "LOW RISK ✅"

    if any(
        filename.endswith(ext)
        for ext in risky_extensions
    ):

        risk_level = "HIGH RISK ⚠️"

    return risk_level

def sync_nodes(blockchain):

    node_files = [

        'node1.json',
        'node2.json',
        'node3.json'
    ]

    for node in node_files:

        with open(node, 'w') as file:

            json.dump(
                blockchain,
                file,
                indent=4
            )

